package com.tarena.weather.entity;

public class Weather {
	private String dayWeather;
	private String dayTemapure;
	private String nightTemapure;
	private String week;
	public String getDayWeather() {
		return dayWeather;
	}
	public void setDayWeather(String dayWeather) {
		this.dayWeather = dayWeather;
	}
	public String getDatTemapure() {
		return dayTemapure;
	}
	public void setDayTemapure(String datTemapure) {
		this.dayTemapure = datTemapure;
	}
	public String getNightTemapure() {
		return nightTemapure;
	}
	public void setNightTemapure(String nightTemapure) {
		this.nightTemapure = nightTemapure;
	}
	public String getWeek() {
		return week;
	}
	public void setWeek(String week) {
		this.week = week;
	}

}
